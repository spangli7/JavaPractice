package com.finalexam.roadsideChecks;

public class AllType {
    int motorcycles;
    int truck;
    int bus;
    int car;

    public AllType() {
    }

    public int getMotorcycles() {
        return motorcycles;
    }

    public void setMotorcycles(int motorcycles) {
        this.motorcycles = motorcycles;
    }

    public int getTruck() {
        return truck;
    }

    public void setTruck(int truck) {
        this.truck = truck;
    }

    public int getBus() {
        return bus;
    }

    public void setBus(int bus) {
        this.bus = bus;
    }

    public int getCar() {
        return car;
    }

    public void setCar(int car) {
        this.car = car;
    }
}
